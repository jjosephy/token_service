	public static RequestContext NewRequestContext(HttpServletRequest request) 
		throws AuthenticationException {

		String header = request.getHeader("api-version");
		if (header == null || header.length() == 0) {
			throw new UnsupportedAuthVersionException();
		}
		
		double version = 0;
		try {
			version = Double.parseDouble(header);
		} catch (NumberFormatException nEx) {
			throw new UnsupportedAuthVersionException();
		}
		
		if (!supportedVersions.contains(version)) {
			throw new UnsupportedAuthVersionException();
		}
		
		StringBuilder stringBuilder = new StringBuilder();
		BufferedReader bufferedReader = null;
		try {
			InputStream inputStream = request.getInputStream();
			if (inputStream != null) {
				bufferedReader = new BufferedReader(new InputStreamReader(inputStream));
				char[] charBuffer = new char[128];
				int bytesRead = -1;
				while ((bytesRead = bufferedReader.read(charBuffer)) > 0) {
					stringBuilder.append(charBuffer, 0, bytesRead);
					if (stringBuilder.length() > 10000) {
						// should not need an input this size, just break and see what happens. 
						break;
					}
				}
			} else {
				stringBuilder.append("");
			}
		} catch (IOException ex) {
			throw new UnauthenticatedException();
		} finally {
			if (bufferedReader != null) {
				try {
					bufferedReader.close();
				} catch (IOException iox) {
					// how to handle?
				}
			}
		}
		
		return new RequestContext(version, stringBuilder.toString(), request);
	}
	
